def _build_preferences_section(preferences: Dict[str, Any]) -> List[str]:
    """Build the Markdown lines for the user preferences section."""
    excl: List[str] = preferences.get("exclude", []) or []
    excl_str: str = ", ".join(excl) if excl else "None"

    lines: List[str] = [
        "## 📋 Your Preferences",
        "",
        f"| Field | Value |",
        f"|-------|-------|",
        f"| Diet type | `{preferences.get('diet') or 'Any'}` |",
        f"| Calorie limit | `{preferences.get('calorie_limit') or 'No limit'}` kcal |",
        f"| Exclusions / Allergens | `{excl_str}` |",
        f"| Cuisine preference | `{preferences.get('cuisine') or 'Any'}` |",
        "",
        "---",
        "",
    ]
    return lines


def _build_meal_card(meal: Dict[str, Any]) -> List[str]:
    """Build Markdown lines for a single meal recommendation card."""
    name: str = str(meal.get("name", "Unknown Meal"))
    badge: str = str(meal.get("badge", f"#{meal.get('rank', '?')} Recommended"))
    score: float = _safe_float(meal.get("final_score", meal.get("score", 0)))
    nutrition_score: float = _safe_float(meal.get("nutrition_score", 0))
    category: str = str(meal.get("category", "unknown")).capitalize()
    reason: str = str(meal.get("reason", "No reason provided."))
    health_flags: List[str] = meal.get("health_flags", []) or []
    calories: Any = meal.get("calories", "N/A")
    protein: Any = meal.get("protein", "N/A")
    fat: Any = meal.get("fat", "N/A")
    carbs: Any = meal.get("carbs", "N/A")
    cuisine: str = str(meal.get("cuisine", "N/A"))
    diet_type: str = str(meal.get("diet_type", "N/A"))

    bar_filled: int = round(score * 10)
    score_bar: str = "█" * bar_filled + "░" * (10 - bar_filled)

    lines: List[str] = [
        f"### {badge}  —  {name}",
        "",
        f"**Score:** `{score:.2f}` `[{score_bar}]`  "
        f"**Nutrition:** `{nutrition_score:.2f}`  "
        f"**Category:** {category}  "
        f"**Cuisine:** {cuisine}",
        "",
        "#### 🧪 Nutritional Breakdown",
        "",
        "| Calories | Protein | Fat | Carbs |",
        "|:--------:|:-------:|:---:|:-----:|",
        f"| {calories} kcal | {protein} g | {fat} g | {carbs} g |",
        "",
        f"**Diet type:** `{diet_type}`",
        "",
        "#### 💡 Why This Meal Was Recommended",
        "",
        f"> {reason}",
        "",
    ]

    if health_flags:
        flag_display: str = ", ".join(f"`{f}`" for f in health_flags)
        lines += [
            f"⚠️  **Health Flags:** {flag_display}",
            "",
        ]

    lines += ["---", ""]
    return lines


def _build_comparison_table(meals: List[Dict[str, Any]]) -> List[str]:
    """Build the Markdown score comparison table section."""
    lines: List[str] = [
        "## 📊 Score Comparison",
        "",
        "| Rank | Meal | Final Score | Nutrition Score | Category | Health Flags |",
        "|:----:|------|:-----------:|:---------------:|:--------:|:-------------|",
    ]
    for m in meals:
        score: float = _safe_float(m.get("final_score", m.get("score", 0)))
        nutr: float = _safe_float(m.get("nutrition_score", 0))
        flags: str = ", ".join(m.get("health_flags", []) or []) or "None"
        cat: str = str(m.get("category", "?")).capitalize()
        lines.append(
            f"| {m.get('rank', '?')} "
            f"| {m.get('name', '?')} "
            f"| **{score:.2f}** "
            f"| {nutr:.2f} "
            f"| {cat} "
            f"| {flags} |"
        )
    lines += [""]
    return lines


def _build_statistics_section(meals: List[Dict[str, Any]]) -> List[str]:
    """Build the aggregate statistics section."""
    avg_cal: float = _safe_avg([m.get("calories") for m in meals])
    avg_prot: float = _safe_avg([m.get("protein") for m in meals])
    avg_score: float = _safe_avg([m.get("final_score", m.get("score", 0)) for m in meals])
    healthy_count: int = sum(1 for m in meals if str(m.get("category", "")).lower() == "healthy")
    flag_counts: Dict[str, int] = {}
    for m in meals:
        for flag in (m.get("health_flags") or []):
            flag_counts[flag] = flag_counts.get(flag, 0) + 1

    lines: List[str] = [
        "## 📈 Summary Statistics",
        "",
        f"- **Total recommendations returned:** {len(meals)}",
        f"- **Average calories:** {avg_cal:.1f} kcal",
        f"- **Average protein:** {avg_prot:.1f} g",
        f"- **Average final score:** {avg_score:.2f}",
        f"- **Meals rated 'healthy':** {healthy_count} / {len(meals)}",
    ]

    if flag_counts:
        flags_str: str = ", ".join(f"{k}: {v}" for k, v in flag_counts.items())
        lines.append(f"- **Health flags detected across results:** {flags_str}")

    lines += [""]
    return lines


def _safe_float(value: Any) -> float:
    """Safely convert any value to float, returning 0.0 on failure."""
    if value is None:
        return 0.0
    try:
        return float(value)
    except (TypeError, ValueError):
        return 0.0


def _safe_avg(values: List[Any]) -> float:
    """Return the mean of a list, safely skipping None/non-numeric values."""
    valid: List[float] = []
    for v in values:
        try:
            valid.append(float(v))
        except (TypeError, ValueError):
            continue
    return sum(valid) / len(valid) if valid else 0.0

def _safe_filename_prefix(prefix: str) -> str:
    """
    Convert a filename prefix into a safe local filename component.

    Args:
        prefix: Raw filename prefix.

    Returns:
        Safe filename prefix containing only letters, numbers, underscore, or dash.
    """
    cleaned = "".join(
        ch for ch in prefix.strip().replace(" ", "_")
        if ch.isalnum() or ch in ("_", "-")
    )
    return cleaned or "recommendation"